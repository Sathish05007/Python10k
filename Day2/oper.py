# #ASSINMENT
a=20
b=30
print(a+b)  # Addition
print(a-b)  # Subtraction       
print(a*b)  # Multiplication
print(a/b)  # Division
print(a//b) # Floor Division
print(a%b)  # Modulus
print(a**b) # Exponentiation


# COMPARISON
print(a==b) # Equal to
print(a!=b) # Not equal to
print(a>b)  # Greater than
print(a<b)  # Less than
print(a>=b) # Greater than or equal to
print(a<=b) # Less than or equal to

#LOGICAL
print(a>10 and b<40) # Logical AND
print(a>10 or b<20)  # Logical OR
print(not(a>10))     # Logical NOT

#MENBERSHIP
print(a in [10, 20, 30]) # Membership in list
