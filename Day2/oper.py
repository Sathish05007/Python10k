# #BEETWISE
a=4
b=2
print(a & b)  # Bitwise AND
print(a | b)  # Bitwise OR  
print(a ^ b)  # Bitwise XOR
print(~a)     # Bitwise NOT
print(a << 2) # Bitwise left shift
print(a >> 2) # Bitwise right shift